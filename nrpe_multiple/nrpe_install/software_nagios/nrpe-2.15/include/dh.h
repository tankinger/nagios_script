#ifndef HEADER_DH_H
#include <openssl/dh.h>
#endif
DH *get_dh512()
	{
	static unsigned char dh512_p[]={
		0xAA,0x0A,0x1C,0x19,0x53,0xE3,0xB0,0x56,0x89,0xDF,0xC6,0x42,
		0x74,0xC9,0xBB,0x33,0x0D,0x78,0x0E,0xB2,0x18,0xA0,0x73,0x1E,
		0x82,0xEF,0x2F,0x89,0x00,0x55,0x44,0xF3,0xE9,0x00,0xAC,0xE7,
		0xE5,0x30,0x29,0x8F,0xAD,0x5A,0x09,0x01,0xCE,0x3B,0x77,0xE8,
		0x79,0xB3,0x61,0xCA,0x02,0xEF,0x86,0x1C,0x89,0x3A,0x0E,0xD6,
		0x6C,0x42,0xCA,0x9B,
		};
	static unsigned char dh512_g[]={
		0x02,
		};
	DH *dh;

	if ((dh=DH_new()) == NULL) return(NULL);
	dh->p=BN_bin2bn(dh512_p,sizeof(dh512_p),NULL);
	dh->g=BN_bin2bn(dh512_g,sizeof(dh512_g),NULL);
	if ((dh->p == NULL) || (dh->g == NULL))
		{ DH_free(dh); return(NULL); }
	return(dh);
	}
